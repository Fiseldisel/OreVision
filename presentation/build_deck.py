def bullets(s, x, y, w, h, items, size=14, gap=7, color=INK, line=1.08):
    box = s.shapes.add_textbox(Inches(x), Inches(y), Inches(w), Inches(h))
    tf = box.text_frame
    tf.word_wrap = True
    tf.margin_left = tf.margin_right = tf.margin_top = tf.margin_bottom = 0
    for i, it in enumerate(items):
        head, _, tail = it.partition("|")
        p = tf.paragraphs[0] if i == 0 else tf.add_paragraph()
        p.space_after = Pt(gap)
        p.line_spacing = line
        r = p.add_run()
        r.text = "‣ "
        r.font.size = Pt(size)
        r.font.color.rgb = ACC
        r.font.bold = True
        r.font.name = B_FONT
        r2 = p.add_run()
        r2.text = head.strip()
        r2.font.size = Pt(size)
        r2.font.bold = bool(tail)
        r2.font.color.rgb = color
        r2.font.name = B_FONT
        if tail:
            r3 = p.add_run()
            r3.text = " — " + tail.strip()
            r3.font.size = Pt(size)
            r3.font.color.rgb = color
            r3.font.name = B_FONT
    return box


