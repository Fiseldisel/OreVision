def card(s, x, y, w, h, fill=TINT, line_c=None):
    c = s.shapes.add_shape(
        MSO_SHAPE.ROUNDED_RECTANGLE, Inches(x), Inches(y), Inches(w), Inches(h)
    )
    c.adjustments[0] = 0.06
    c.fill.solid()
    c.fill.fore_color.rgb = fill
    if line_c is None:
        c.line.fill.background()
    else:
        c.line.color.rgb = line_c
        c.line.width = Pt(0.75)
    c.shadow.inherit = False
    return c


def stat(s, x, y, w, num, label, num_color=ACC, num_size=44, label_color=MUT):
    txt(s, x, y, w, 0.8, num, size=num_size, bold=True, font=H_FONT,
        color=num_color, align=PP_ALIGN.CENTER)
