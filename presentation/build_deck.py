def slide_new(bg=PAPER):
    s = prs.slides.add_slide(BLANK)
    r = s.shapes.add_shape(MSO_SHAPE.RECTANGLE, 0, 0, prs.slide_width, prs.slide_height)
    r.fill.solid()
    r.fill.fore_color.rgb = bg
    r.line.fill.background()
    r.shadow.inherit = False
    return s


def txt(s, x, y, w, h, runs, size=15, color=INK, bold=False, font=B_FONT,
        align=PP_ALIGN.LEFT, anchor=MSO_ANCHOR.TOP, space_after=6, line=None):
    """runs: str | list[ (text, {overrides}) ] — каждый элемент = абзац."""
    box = s.shapes.add_textbox(Inches(x), Inches(y), Inches(w), Inches(h))
    tf = box.text_frame
    tf.word_wrap = True
    tf.vertical_anchor = anchor
    tf.margin_left = tf.margin_right = tf.margin_top = tf.margin_bottom = 0
    if isinstance(runs, str):
        runs = [(runs, {})]
    for i, (t, ov) in enumerate(runs):
        p = tf.paragraphs[0] if i == 0 else tf.add_paragraph()
        p.alignment = ov.get("align", align)
        p.space_after = Pt(ov.get("space_after", space_after))
        if line:
            p.line_spacing = line
        r = p.add_run()
        r.text = t
        f = r.font
        f.size = Pt(ov.get("size", size))
        f.bold = ov.get("bold", bold)
        f.italic = ov.get("italic", False)
        f.name = ov.get("font", font)
        f.color.rgb = ov.get("color", color)
    return box


def rich(s, x, y, w, h, paras, size=14, color=INK, space_after=7, line=1.06):
    """paras: list of lists of (text, overrides) — многорановые абзацы."""
    box = s.shapes.add_textbox(Inches(x), Inches(y), Inches(w), Inches(h))
    tf = box.text_frame
    tf.word_wrap = True
    tf.margin_left = tf.margin_right = tf.margin_top = tf.margin_bottom = 0
    for i, runs in enumerate(paras):
        p = tf.paragraphs[0] if i == 0 else tf.add_paragraph()
        p.space_after = Pt(space_after)
        p.line_spacing = line
        for t, ov in runs:
            r = p.add_run()
            r.text = t
            f = r.font
            f.size = Pt(ov.get("size", size))
            f.bold = ov.get("bold", False)
            f.italic = ov.get("italic", False)
            f.name = ov.get("font", B_FONT)
            f.color.rgb = ov.get("color", color)
    return box


