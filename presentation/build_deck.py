def pic(s, path, x, y, max_w, max_h, align="center"):
    """Изображение, вписанное в прямоугольник с сохранением пропорций."""
    with Image.open(path) as im:
        iw, ih = im.size
    w_in = iw / ih * (max_h) if (iw / ih) * max_h <= max_w else max_w
    h_in = w_in * ih / iw
    if h_in > max_h:
        h_in = max_h
        w_in = h_in * iw / ih
    px = x + (max_w - w_in) / 2 if align == "center" else x
    py = y + (max_h - h_in) / 2
    return s.shapes.add_picture(str(path), Inches(px), Inches(py),
                                Inches(w_in), Inches(h_in))


def chip(s, x, y, w, h, text, fill, text_color=PAPER, size=13, bold=True):
    c = card(s, x, y, w, h, fill=fill)
    tf = c.text_frame
    tf.word_wrap = True
    tf.margin_left = tf.margin_right = Inches(0.06)
    tf.margin_top = tf.margin_bottom = Inches(0.02)
    p = tf.paragraphs[0]
    p.alignment = PP_ALIGN.CENTER
    r = p.add_run()
    r.text = text
    r.font.size = Pt(size)
    r.font.bold = bold
    r.font.name = B_FONT
    r.font.color.rgb = text_color
    tf.vertical_anchor = MSO_ANCHOR.MIDDLE
    return c


